package nc.bs.export.file.dbo;

public class UnsupportedDBTypeException extends Exception {

	private static final long serialVersionUID = -3386388953987182608L;

	public UnsupportedDBTypeException()
	{
	}

	public UnsupportedDBTypeException(String message)
	{
		super(message);
	}

	public UnsupportedDBTypeException(Throwable cause)
	{
		super(cause);
	}

	public UnsupportedDBTypeException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
